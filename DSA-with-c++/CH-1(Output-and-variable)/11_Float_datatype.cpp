#include<iostream>
#include<bits/stdc++.h>
using namespace std;

int main(){
    float x=5;  //! float can store integer as well not just float value 
    float y=2;
    cout<<x/y<<endl;
    cout<<5/2<<endl;

    //! Few operations on float and int dataType 
    cout<<5/2;        //* 2 
    cout<<5.0/2;      //* 2.5
    cout<<5/2.0;      //* 2.5
    cout<<5.0/2.0;    //* 2.5
        
    return 0;
}